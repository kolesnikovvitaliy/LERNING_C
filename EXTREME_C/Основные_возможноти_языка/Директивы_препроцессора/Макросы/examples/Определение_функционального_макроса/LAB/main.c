/**
 * @file defining_funtional_macro.c
 * @brief Роскошный демонстрационный модуль, воспевающий классическую микромагию препроцессора Си.
 *
 * Данный файл представляет собой фундаментальный пример использования макроопределений
 * и базовой арифметики. Каждая строчка кода снабжена монументальным описанием
 * для автоматической генерации документации Doxygen.
 */

#include <stdio.h>

/**
 * @brief Высокоскоростной макрос-калькулятор для бинарного сложения.
 *
 * @details Этот макрос является классическим элементом языка Си. Он выполняет
 * текстуальную подстановку на этапе препроцессинга, заменяя вызов макроса на
 * прямое математическое выражение сложения двух операндов.
 *
 * Все аргументы и само выражение бережно заключены в круглые скобки. Это гарантирует
 * незыблемость приоритета операций, даже если в качестве аргументов будут переданы
 * сложные выражения (например, \c ADD(x & y, z) ).
 *
 * @warning В отличие от функций, макрос не проверяет типы данных! Вы можете передать
 * туда как целые числа, так и числа с плавающей точкой или указатели. Будьте осторожны.
 *
 * @param a Левый операнд (слагаемое). Может быть любым базовым типом, поддерживающим оператор '+'.
 * @param b Правый операнд (слагаемое). Магически соединяется с первым операндом.
 *
 * @return Суммарный результат слияния двух операндов. Тип возвращаемого значения
 *         автоматически выводится компилятором на основе типов аргументов \p a и \p b.
 */
#define ADD(a, b) ((a) + (b))

/**
 * @brief Главная оркестровая сцена и точка входа в жизненный цикл программы.
 *
 * @details Функция \c main инициализирует выполнение всего программного кода
 * в пространстве операционной системы. Здесь разворачивается главное действие:
 * объявление переменных, их инициализация константами и последующий вызов
 * макроса \ref ADD для получения итогового математического триумфа.
 *
 * @param[in] argc Священный счетчик аргументов, содержащий количество параметров командной строки.
 * @param[in] argv Массив указателей на строки, бережно хранящий текстовые аргументы запуска.
 *
 * @return int Статус успешности завершения земного пути нашей программы.
 * @retval 0  Абсолютный успех. Программа отработала безукоризненно, все числа сложились корректно.
 * @retval 1+ Код ошибки, сигнализирующий о непредвиденных катаклизмах (в данном сценарии исключено).
 */
int main(int argc, char **argv) {
    /**
     * @brief Локальная переменная 'x'.
     * Первое целое число (int), выступающее фундаментом нашего вычисления.
     * Инициализировано строгим значением 2. Occupies 4 bytes of memory standardly.
     */
    int x = 2;

    /**
     * @brief Локальная переменная 'y'.
     * Второе целое число (int), призванное дополнить первое.
     * Намертво связано с константой 5 в стеке программы.
     */
    int y = 5;

    /**
     * @brief Локальная переменная 'z'.
     * Священный грааль нашей программы, куда записывается результат сложения.
     * На этапе компиляции строка превратится в \c int \c z \c = \c ((x) \c + \c (y));
     * В текущих реалиях здесь родится и будет храниться число 7.
     */
    int z = ADD(x, y);

    /* Подавляем стенания компилятора о неиспользуемых системных переменных */
    (void)argc;
    (void)argv;
    (void)z;

    /**
     * @brief Финальный аккорд.
     * Мы осознанно заменили некорректный суффикс '0l' (long) на чистый '0',
     * чтобы тип возвращаемого значения идеально соответствовал сигнатуре int main.
     * Возвращаем ноль, как символ безупречной победы алгоритма над кремнием.
     */
    return 0;
}

